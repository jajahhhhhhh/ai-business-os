"""Repository and gateway interfaces the use cases depend on.

All are structural Protocols so infrastructure can satisfy them with
SQLAlchemy models without the application layer importing the ORM.
"""

from __future__ import annotations

import uuid
from collections.abc import Sequence
from datetime import date, datetime
from decimal import Decimal
from typing import Any, Protocol

from src.domain.cursor import Cursor
from src.domain.leads import LeadStage


class AuditWriter(Protocol):
    async def write(
        self,
        actor: str,
        action: str,
        entity: str,
        entity_id: uuid.UUID | None,
        diff: dict[str, Any] | None,
    ) -> None: ...


# ------------------------------------------------------------------ row shapes


class SiteRow(Protocol):
    @property
    def id(self) -> uuid.UUID: ...
    @property
    def name(self) -> str: ...
    @property
    def location(self) -> str | None: ...
    @property
    def budget_thb(self) -> Decimal | None: ...
    @property
    def created_at(self) -> datetime: ...


class ContractorRow(Protocol):
    @property
    def id(self) -> uuid.UUID: ...
    @property
    def name(self) -> str: ...
    @property
    def contact(self) -> str | None: ...
    @property
    def line_id(self) -> str | None: ...


class QuotationRow(Protocol):
    @property
    def id(self) -> uuid.UUID: ...
    @property
    def site_id(self) -> uuid.UUID: ...
    @property
    def contractor_id(self) -> uuid.UUID: ...
    @property
    def category(self) -> str: ...
    @property
    def amount_thb(self) -> Decimal: ...
    @property
    def status(self) -> str: ...
    @property
    def created_at(self) -> datetime: ...


class DrawRow(Protocol):
    @property
    def id(self) -> uuid.UUID: ...
    @property
    def quotation_id(self) -> uuid.UUID: ...
    @property
    def seq(self) -> int: ...
    @property
    def amount_thb(self) -> Decimal: ...
    @property
    def status(self) -> str: ...
    @property
    def requested_at(self) -> datetime: ...
    @property
    def paid_at(self) -> datetime | None: ...


class SpendRow(Protocol):
    """One (site, category) aggregation row."""

    @property
    def site_id(self) -> uuid.UUID: ...
    @property
    def category(self) -> str: ...
    @property
    def quoted_thb(self) -> Decimal: ...
    @property
    def paid_thb(self) -> Decimal: ...
    @property
    def pending_thb(self) -> Decimal: ...


class DrawDisplayRow(Protocol):
    """A draw enriched with quotation/contractor/site context for display."""

    @property
    def id(self) -> uuid.UUID: ...
    @property
    def seq(self) -> int: ...
    @property
    def amount_thb(self) -> Decimal: ...
    @property
    def status(self) -> str: ...
    @property
    def requested_at(self) -> datetime: ...
    @property
    def paid_at(self) -> datetime | None: ...
    @property
    def quotation_id(self) -> uuid.UUID: ...
    @property
    def category(self) -> str: ...
    @property
    def contractor_name(self) -> str: ...
    @property
    def site_id(self) -> uuid.UUID: ...
    @property
    def site_name(self) -> str: ...


class MilestoneRow(Protocol):
    @property
    def id(self) -> uuid.UUID: ...
    @property
    def site_id(self) -> uuid.UUID: ...
    @property
    def name(self) -> str: ...
    @property
    def planned_date(self) -> date | None: ...
    @property
    def actual_date(self) -> date | None: ...
    @property
    def status(self) -> str: ...
    @property
    def created_at(self) -> datetime: ...


class BankTransactionRow(Protocol):
    @property
    def id(self) -> uuid.UUID: ...
    @property
    def occurred_at(self) -> datetime: ...
    @property
    def amount_thb(self) -> Decimal: ...
    @property
    def direction(self) -> str: ...
    @property
    def bank(self) -> str: ...
    @property
    def account_tail(self) -> str | None: ...
    @property
    def raw_text(self) -> str: ...
    @property
    def source(self) -> str: ...
    @property
    def status(self) -> str: ...
    @property
    def matched_draw_id(self) -> uuid.UUID | None: ...
    @property
    def ambiguous_match(self) -> bool: ...
    @property
    def dedup_hash(self) -> str: ...
    @property
    def created_at(self) -> datetime: ...


class ReportRow(Protocol):
    @property
    def id(self) -> uuid.UUID: ...
    @property
    def kind(self) -> str: ...
    @property
    def period(self) -> str | None: ...
    @property
    def lang(self) -> str: ...
    @property
    def body(self) -> str | None: ...
    @property
    def sent_at(self) -> datetime | None: ...
    @property
    def created_at(self) -> datetime: ...


class LeadRow(Protocol):
    @property
    def id(self) -> uuid.UUID: ...
    @property
    def kind(self) -> str: ...
    @property
    def name(self) -> str: ...
    @property
    def locale(self) -> str | None: ...
    @property
    def intent_score(self) -> int: ...
    @property
    def stage(self) -> str: ...
    @property
    def first_seen_at(self) -> datetime: ...
    @property
    def last_activity_at(self) -> datetime | None: ...
    @property
    def created_at(self) -> datetime: ...


# ------------------------------------------------------------------ repositories


class RenovationRepository(Protocol):
    async def create_site(
        self, name: str, location: str | None, budget_thb: Decimal | None
    ) -> SiteRow: ...
    async def list_sites(self) -> Sequence[SiteRow]: ...
    async def get_site(self, site_id: uuid.UUID) -> SiteRow | None: ...
    async def create_contractor(
        self, name: str, contact: str | None, line_id: str | None
    ) -> ContractorRow: ...
    async def get_contractor(self, contractor_id: uuid.UUID) -> ContractorRow | None: ...
    async def create_quotation(
        self,
        site_id: uuid.UUID,
        contractor_id: uuid.UUID,
        category: str,
        amount_thb: Decimal,
        status: str,
    ) -> QuotationRow: ...
    async def get_quotation(self, quotation_id: uuid.UUID) -> QuotationRow | None: ...
    async def list_draws(self, quotation_id: uuid.UUID) -> Sequence[DrawRow]: ...
    async def create_draw(
        self, quotation_id: uuid.UUID, seq: int, amount_thb: Decimal
    ) -> DrawRow: ...
    async def get_draw(self, draw_id: uuid.UUID) -> DrawRow | None: ...
    async def mark_draw_paid(self, draw_id: uuid.UUID, paid_at: datetime) -> DrawRow: ...
    async def spend_rows(self) -> Sequence[SpendRow]: ...
    async def list_quotations(self, site_id: uuid.UUID) -> Sequence[QuotationRow]: ...
    async def list_draws_display(
        self, site_id: uuid.UUID | None, status: str | None
    ) -> Sequence[DrawDisplayRow]: ...
    async def list_milestones(self, site_id: uuid.UUID) -> Sequence[MilestoneRow]: ...
    async def get_milestone(self, milestone_id: uuid.UUID) -> MilestoneRow | None: ...
    async def create_milestone(
        self, site_id: uuid.UUID, name: str, planned_date: date | None
    ) -> MilestoneRow: ...
    async def update_milestone(
        self, milestone_id: uuid.UUID, changes: dict[str, Any]
    ) -> MilestoneRow: ...


class BankTransactionRepository(Protocol):
    async def get(self, tx_id: uuid.UUID) -> BankTransactionRow | None: ...
    async def get_by_dedup_hash(self, dedup_hash: str) -> BankTransactionRow | None: ...
    async def create(
        self,
        *,
        occurred_at: datetime,
        amount_thb: Decimal,
        direction: str,
        bank: str,
        account_tail: str | None,
        raw_text: str,
        source: str,
        status: str,
        matched_draw_id: uuid.UUID | None,
        ambiguous_match: bool,
        dedup_hash: str,
    ) -> BankTransactionRow: ...
    async def list(self, status: str | None, limit: int) -> Sequence[BankTransactionRow]: ...
    async def set_match(
        self,
        tx_id: uuid.UUID,
        *,
        status: str,
        matched_draw_id: uuid.UUID | None,
        ambiguous_match: bool,
    ) -> BankTransactionRow: ...
    async def set_status(self, tx_id: uuid.UUID, status: str) -> BankTransactionRow: ...
    async def list_pending_draws(self) -> Sequence[DrawRow]: ...
    async def get_draw(self, draw_id: uuid.UUID) -> DrawRow | None: ...


class LeadRepository(Protocol):
    async def get(self, lead_id: uuid.UUID) -> LeadRow | None: ...
    async def list_page(
        self,
        *,
        stage: LeadStage | None,
        min_score: int | None,
        q: str | None,
        after: Cursor | None,
        limit: int,
    ) -> Sequence[LeadRow]: ...
    async def set_stage(
        self, lead_id: uuid.UUID, stage: LeadStage, activity_at: datetime
    ) -> LeadRow: ...
    async def add_event(
        self,
        lead_id: uuid.UUID,
        event_type: str,
        payload: dict[str, Any] | None,
        occurred_at: datetime,
    ) -> None: ...
